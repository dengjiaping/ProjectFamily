package com.chuxin.family.parse.been.data;

public class AnswerHomeRateItem {

	private String groupId;
	private String rate;
	
	private String wifeName;
	private String wifeUrl;
	private String husbandName;
	private String husbandUrl;
	
	
	public String getWifeName() {
		return wifeName;
	}
	public void setWifeName(String wifeName) {
		this.wifeName = wifeName;
	}
	public String getWifeUrl() {
		return wifeUrl;
	}
	public void setWifeUrl(String wifeUrl) {
		this.wifeUrl = wifeUrl;
	}
	public String getHusbandName() {
		return husbandName;
	}
	public void setHusbandName(String husbandName) {
		this.husbandName = husbandName;
	}
	public String getHusbandUrl() {
		return husbandUrl;
	}
	public void setHusbandUrl(String husbandUrl) {
		this.husbandUrl = husbandUrl;
	}
	
	
	public String getGroupId() {
		return groupId;
	}
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	
	
	
	
	
}
